#Efetuar a leitura de dois valores numéricos inteiros representados pelas variáveis A e B
#e apresentar o resultado da diferença A-B.
#entradada de dados
valorA = int(input('digite o valor de A '))
valorB = int(input('digite o valor de B '))

#processamento
subtracao = valorA - valorB

#saida
print(f'a diferença entre os resultados de A e B é {subtracao} ')